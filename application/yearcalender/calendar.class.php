            $cellContent = $this->currentDay;
             
            $this->currentDay++;   
             
        }else{
             
            $this->currentDate =null;
 
            $cellContent=null;
        }
        if($cellContent){
            $deptyearevents = DeptYearEvents::LoadAll();
            /*
