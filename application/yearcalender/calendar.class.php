                    QQ::GreaterOrEqual(QQN::DeptYearEvents()->From, date('20160116000000')),
                    QQ::LessOrEqual(QQN::DeptYearEvents()->To, date('20160116235959'))
                    //QQ::GreaterOrEqual(QQN::DeptYearEvents()->From, date('Ymd000000',strtotime($this->currentDate))),
                    //QQ::LessOrEqual(QQN::DeptYearEvents()->To, date('Ymd235959',strtotime($this->currentDate)))
                    ));             
             */
                        
            
                $data = "";
