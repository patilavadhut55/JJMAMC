                        ($cellContent==null?'mask':'').'"><div style="font-size: 19px; color:black;">'.$cellContent.'</div>'.$data.'</div>';
                
        }else{
            return '<div style="margin-right:5px; border:1px solid lightgrey;height:100px;width:80px;float:left;" id="li-'.$this->currentDate.'" class="'.($cellNumber%7==1?' start ':($cellNumber%7==0?' end ':' ')).
                ($cellContent==null?'mask':'').'">'.$cellContent.'</div>';
